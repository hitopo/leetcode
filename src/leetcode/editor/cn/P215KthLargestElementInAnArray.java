    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int findKthLargest(int[] nums, int k) {
            // å¿«é€Ÿé€‰æ‹©ç®—æ³•
            int n = nums.length;
            int target = n - k;
            int l = 0;
            int r = n - 1;
            while (true) {
                int idx = partition(nums, l, r);
                if (idx < target) {
                    l = idx + 1;
                } else if (idx > target) {
                    r = idx - 1;
                } else {
                    return nums[idx];
                }
            }
        }

        private int partition(int[] nums, int l, int r) {
            int pivot = nums[l];
            while (l < r) {
                while (l < r && nums[r] >= pivot) {
                    r--;
                }
                if (l < r) {
                    nums[l] = nums[r];
                    l++;
                }
                while (l < r && nums[l] <= pivot) {
                    l++;
                }
                if (l < r) {
                    nums[r] = nums[l];
                    r--;
                }
            }
            nums[l] = pivot;
            return l;
        }
    }
